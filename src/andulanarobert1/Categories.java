    private void jcatsearchbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcatsearchbuttonActionPerformed
        // TODO add your handling code here:
           String search = jsearchcat.getText().trim();

    Connection con = DatabaseConnection.dbConnection();
    DefaultTableModel dtm = (DefaultTableModel) jcattable.getModel();
    dtm.setRowCount(0);

    try {
        String sql = "SELECT category_name, description, date_added " +
                     "FROM categories " +
                     "WHERE category_name LIKE ? OR description LIKE ? " +
                     "ORDER BY category_name ASC";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, "%" + search + "%");
        pst.setString(2, "%" + search + "%");

        ResultSet rs = pst.executeQuery();
        boolean found = false;

        while (rs.next()) {
            found = true;

            Object obj[] = {
                rs.getString("category_name"),
                rs.getString("description"),
                rs.getString("date_added")
            };

            dtm.addRow(obj);
        }

        if (!found) {
            JOptionPane.showMessageDialog(null, "No matching category found.");
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error searching categories: " + ex.getMessage());
    }

    }//GEN-LAST:event_jcatsearchbuttonActionPerformed

    private void jcatupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcatupdateActionPerformed
        // TODO add your handling code here:
      int selectedRow = jcattable.getSelectedRow();

    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a category first.");
        return;
    }

    String oldName = jcattable.getValueAt(selectedRow, 0).toString();
    String newName = jcatfield.getText().trim();
    String description = jtadesc.getText().trim();

    if (newName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Category name cannot be empty.");
        return;
    }

    try {
        Connection con = DatabaseConnection.dbConnection();

        String sql = "UPDATE categories SET category_name=?, description=? WHERE category_name=?";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, newName);
        pst.setString(2, description);
        pst.setString(3, oldName);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Category updated successfully.");

        clearFields();
        loadCategoryTable();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    }//GEN-LAST:event_jcatupdateActionPerformed

    private void jdeletecatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jdeletecatActionPerformed
        // TODO add your handling code here:
        int selectedRow = jcattable.getSelectedRow();

    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a category first.");
        return;
    }

    String categoryName = jcattable.getValueAt(selectedRow, 0).toString();

    int confirm = JOptionPane.showConfirmDialog(this,
            "Delete '" + categoryName + "'?",
            "Confirm", JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        Connection con = DatabaseConnection.dbConnection();

        // ✅ Check if products are linked first
        String checkSQL = "SELECT COUNT(*) FROM products p " +
                          "JOIN categories c ON p.category_id = c.category_id " +
                          "WHERE c.category_name = ?";
        PreparedStatement checkStmt = con.prepareStatement(checkSQL);
        checkStmt.setString(1, categoryName);
        ResultSet rs = checkStmt.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this,
                "Cannot delete! This category has " + rs.getInt(1) + " product(s) linked to it.\n" +
                "Please reassign or delete those products first.",
                "Delete Failed", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ✅ Safe to delete
        PreparedStatement pst = con.prepareStatement(
            "DELETE FROM categories WHERE category_name=?");
        pst.setString(1, categoryName);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Category deleted successfully.");
        loadCategoryTable();
        clearFields();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    
    }//GEN-LAST:event_jdeletecatActionPerformed

    private void jcatcreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcatcreateActionPerformed
        // TODO add your handling code here:
        String categoryName = jcatfield.getText().trim();
        String description = jtadesc.getText().trim();

        if (categoryName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Category name cannot be empty.");
            return;
        }

        try {
            Connection con = DatabaseConnection.dbConnection();
            String checkSql = "SELECT category_id FROM categories WHERE category_name = ?";
            PreparedStatement pstCheck = con.prepareStatement(checkSql);
            pstCheck.setString(1, categoryName);
            ResultSet rsCheck = pstCheck.executeQuery();

            if (rsCheck.next()) {
                JOptionPane.showMessageDialog(this, "Category already exists.");
                return;
            }

            String insertSql = "INSERT INTO categories (category_name, description) VALUES (?, ?)";
            PreparedStatement pstInsert = con.prepareStatement(insertSql);
            pstInsert.setString(1, categoryName);
            pstInsert.setString(2, description);
            pstInsert.executeUpdate();

            JOptionPane.showMessageDialog(this, "Category created successfully!");
            clearFields();
            loadCategoryTable();
            clearFields();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jcatcreateActionPerformed

