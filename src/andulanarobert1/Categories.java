private void loadCategoryTable() {
    Connection con = DatabaseConnection.dbConnection();
    DefaultTableModel dtm = (DefaultTableModel) jcattable.getModel();
    dtm.setRowCount(0); // clear table

    try {
        String sql = "SELECT category_name, description FROM categories ORDER BY category_name ASC";
        ResultSet rs = con.prepareStatement(sql).executeQuery();

        while (rs.next()) {
            Object[] obj = {
                rs.getString("category_name"),
                rs.getString("description")
            };
            dtm.addRow(obj);
        }
    } catch (Exception ex) {
        System.out.println(ex);
    }
}

